package com.test1;

public class VueElement {
	private String developedBy;
	private String competitor;
	private String versatileChange;
	private String nativeHtmlElement;
	private String customComponent;
	private String expressiveTemplate;
	private String declarativeTemplate;
	private Integer angulariveProps;
	private String reRenderingScope;
	private String scopedStyles;
	private String vanillaJavascriptSupport;

	public VueElement() {

	}

	public VueElement(String developedBy, String competitor, String versatileChange, String nativeHtmlElement,
			String customComponent, String expressiveTemplate, String declarativeTemplate, Integer angulariveProps,
			String reRenderingScope, String scopedStyles, String vanillaJavascriptSupport) {
		super();
		this.developedBy = developedBy;
		this.competitor = competitor;
		this.versatileChange = versatileChange;
		this.nativeHtmlElement = nativeHtmlElement;
		this.customComponent = customComponent;
		this.expressiveTemplate = expressiveTemplate;
		this.declarativeTemplate = declarativeTemplate;
		this.angulariveProps = angulariveProps;
		this.reRenderingScope = reRenderingScope;
		this.scopedStyles = scopedStyles;
		this.vanillaJavascriptSupport = vanillaJavascriptSupport;
	}

	public String getDevelopedBy() {
		return developedBy;
	}

	public void setDevelopedBy(String developedBy) {
		this.developedBy = developedBy;
	}

	public String getCompetitor() {
		return competitor;
	}

	public void setCompetitor(String competitor) {
		this.competitor = competitor;
	}

	public String getVersatileChange() {
		return versatileChange;
	}

	public void setVersatileChange(String versatileChange) {
		this.versatileChange = versatileChange;
	}

	public String getNativeHtmlElement() {
		return nativeHtmlElement;
	}

	public void setNativeHtmlElement(String nativeHtmlElement) {
		this.nativeHtmlElement = nativeHtmlElement;
	}

	public String getCustomComponent() {
		return customComponent;
	}

	public void setCustomComponent(String customComponent) {
		this.customComponent = customComponent;
	}

	public String getExpressiveTemplate() {
		return expressiveTemplate;
	}

	public void setExpressiveTemplate(String expressiveTemplate) {
		this.expressiveTemplate = expressiveTemplate;
	}

	public String getDeclarativeTemplate() {
		return declarativeTemplate;
	}

	public void setDeclarativeTemplate(String declarativeTemplate) {
		this.declarativeTemplate = declarativeTemplate;
	}

	public Integer getAngulariveProps() {
		return angulariveProps;
	}

	public void setAngulariveProps(Integer angulariveProps) {
		this.angulariveProps = angulariveProps;
	}

	public String getReRenderingScope() {
		return reRenderingScope;
	}

	public void setReRenderingScope(String reRenderingScope) {
		this.reRenderingScope = reRenderingScope;
	}

	public String getScopedStyles() {
		return scopedStyles;
	}

	public void setScopedStyles(String scopedStyles) {
		this.scopedStyles = scopedStyles;
	}

	public String getVanillaJavascriptSupport() {
		return vanillaJavascriptSupport;
	}

	public void setVanillaJavascriptSupport(String vanillaJavascriptSupport) {
		this.vanillaJavascriptSupport = vanillaJavascriptSupport;
	}

}
