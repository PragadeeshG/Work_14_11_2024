create table if not exists vue_element(
developed_by varchar(255) not null,
competitor varchar(255) null,
versatile_change varchar(255) null,
native_html_element varchar(255) null,
custom_component varchar(255) null,
expressive_template varchar(255) null,
declarative_template varchar(255) null,
angularive_props Integer null,
re_rendering_scope varchar(255) null,
scoped_styles varchar(255) null,
vanilla_javascript_support varchar(255) null,
constraint vue_element_pk primary key(developed_by));